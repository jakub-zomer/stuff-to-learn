package cz.zomer.calculator

fun addition(a: Double, b: Double): Double {
    return a + b
}

fun substraction(a: Double, b: Double): Double {
    return a - b
}

fun multipliction(a: Double, b: Double): Double {
    return a * b
}

fun division(a: Double, b: Double) : Double {
    return a / b
}



